import numpy as np

w = np.array([16, 4, -9]) # задайте самостоятельно (параметры модели: w0, w1, w2 - ориентация разделяющей линии)
x_test = np.array([(1, -5, 2), (1, -4, 7), (1, 3, 2), (1, 3, -3), (1, 5, 6), (1, 9, 2)]) # задайте самостоятельно (признаки образов: x0, x1, x2)
y_test = np.array([1, 1, 1, -1, -1, -1]) # задайте самостоятельно (метки класса)

margin = [(x_test[i] @ w.T) * y_test[i] for i in range(len(x_test))]
print(margin)